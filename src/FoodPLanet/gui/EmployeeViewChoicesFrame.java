    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        
        if(str.equals("AllEmp"))
        {
            EmployeeAllViewFrame obj=new EmployeeAllViewFrame();
            this.dispose();
            obj.setVisible(true);
        }
        else if(str.equals("SingleEmp"))
        {
            EmployeeSingleViewFrame obj= new EmployeeSingleViewFrame();
            this.dispose();
            obj.setVisible(true);
        }
        else
            JOptionPane.showMessageDialog(null,"Please Select An Input.", "INFORMATION",JOptionPane.ERROR_MESSAGE);
            
    }//GEN-LAST:event_btnViewActionPerformed

